import cocotb
from cocotb.clock import Clock
from cocotb.triggers import ClockCycles


@cocotb.test()
async def test_project(dut):
    dut._log.info("Start")

    # Set the clock period to 10 us (100 KHz)
    clock = Clock(dut.clk, 10, units="us")
    cocotb.start_soon(clock.start())

    # Reset
    dut._log.info("Reset")
    dut.ena.value = 1
    dut.ui_in.value = 0
    dut.uio_in.value = 0
    dut.rst_n.value = 0
    await ClockCycles(dut.clk, 10)
    dut.rst_n.value = 1

    dut._log.info("Test project behavior")

    A      = 0
    B      = 1
    AND16  = 2
    AND32  = 3
    OR16   = 4
    OR32   = 5
    XOR16  = 6
    XOR32  = 7
    INT16L = 8
    INT16H = 9
    SEL16  = 10
    SEL32  = 11

    async def alu_write(a, d):
        # Set address and data
        dut.ui_in.value = 0xC0 | a
        dut.uio_in.value = d & 0xFF
        await ClockCycles(dut.clk, 1)
        # Enable write
        dut.ui_in.value = 0x40 | a
        await ClockCycles(dut.clk, 1)
        # Disable write
        dut.ui_in.value = 0xC0 | a
        await ClockCycles(dut.clk, 1)

    async def alu_read(a):
        # Enable output
        dut.ui_in.value = 0x80 | a
        await ClockCycles(dut.clk, 1)
        r1 = dut.uo_out.value & 0xFF
        r2 = dut.uio_out.value & 0xFF
        # Disable output
        dut.ui_in.value = 0xC0 | a
        await ClockCycles(dut.clk, 1)
        assert r1 == r2
        return r1

    async def test_alu(s, a, b, f):
        # Write first operand
        await alu_write(0, a)
        await alu_write(1, a >> 8)
        await alu_write(2, a >> 16)
        await alu_write(3, a >> 24)
        # Write second operand
        await alu_write(4, b)
        await alu_write(5, b >> 8)
        await alu_write(6, b >> 16)
        await alu_write(7, b >> 24)
        # Read result
        r0 = await alu_read((s << 2) + 0)
        r1 = await alu_read((s << 2) + 1)
        r2 = await alu_read((s << 2) + 2)
        r3 = await alu_read((s << 2) + 3)
        r = (r0 | (r1 << 8) | (r2 << 16) | (r3 << 24))
        assert r == f

    async def test_and16(a, y):
        await test_alu(AND16, a, 42, y)
